public class Calculadora {
	/**
	 * Método que suma dos números.
	 * @param a
	 * @param b
	 * @return a+b
	 */
	public int sumar(int a, int b) {
		return a+b;
	}
	/**
	 * Método que resta dos números.
	 * @param a
	 * @param b
	 * @return a-b
	 */
	public int resta(int a, int b) {
		return a-b;
	}
}
